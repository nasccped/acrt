#ifndef _ACRT_RESULT_H_
#define _ACRT_RESULT_H_

#include <stdint.h>

// Store a complex assertion result as tagged union.
typedef struct {

  // Local context of the ran assertion.
  struct {

    // Context name being used.
    const char *name;

    // Assertion line.
    unsigned int line;

  } context;

  // The kind of assertion.
  enum {

    // When the assertion kind must be ignored.
    IGNORED_ASSERTION_KIND,

    // A boolean assertion was invoked (with 'ACRT_BOOL' macro).
    BOOLEAN_ASSERTION_KIND,

    // An eq assertion was invoked (with 'ACRT_EQ' or 'ACRT_EQ_PRINT' macro).
    EQ_ASSERTION_KIND

  } kind;

  // The data being held by this assertion.
  union {

    // Boolean assertion struct (when 'kind' field refers to
    // 'BOOLEAN_ASSERTION_KIND').
    struct {

      // Kind of value that was asserted.
      enum {

        // Pointer-like value was asserted.
        POINTER_VALUE_KIND,

        // Numeric-like value was asserted, and then, cast to integer.
        INTEGER_VALUE_KIND

      } kind;

      // Value literal held by the assertion.
      union {

        // When 'value_kind' field refers to 'POINTER_VALUE'.
        void *pointer;

        // When 'value_kind' field refers to 'INTEGER_VALUE'.
        int integer;

      } value;

    } boolean_assertion;

    // Eq assertion struct (when 'kind' field refers to 'EQ_ASSERTION_KIND').
    struct {

      // Kind of warning generated by the current assertion.
      enum {

        // There's no warning for this assertion.
        NO_WARNING,

        // The eq function points to null and the assertion can't be ran.
        EQ_FUNC_IS_NULL_WARNING,

        // Both params of eq function points to same address.
        SAME_ADDRESS_PARAMS_WARNING,

        // Both params of eq function points to same address but they return
        // false from the 'eq_function'.
        SAME_ADDRESS_PARAMS_ARE_DIFF_WARNING

      } warning;

      // Params used within this assertion.
      struct {

        void
            // Left param.
            *left,
            // Right param.
            *right;

      } params;

      // Result of the current assertion. It's stored to allow eq_function to
      // run one single time.
      int result;

    } eq_assertion;

  } data;
} acrt_result_t;

// When the result fields doesn't matter (just looking for ignored status).
#define IGNORED_ASSERTION_RESULT                                               \
  (acrt_result_t) { .kind = IGNORED_ASSERTION_KIND }

// Returns the assertion kind as string.
char *acrt_result_get_kind_as_str(acrt_result_t *self);

// Returns 1 if self is non-null + inner kind is recognized as ignored.
int acrt_result_is_ignored(acrt_result_t *self);

// Returns 1 if self is non-null + inner status is recognized as passed value,
// otherwise, returns zero.
int acrt_result_is_passed(acrt_result_t *self);

// Returns 1 if self is non-null + inner status is recognized as warning.
int acrt_result_is_warning(acrt_result_t *self);

// Creates a new acrt_result_t from 'intptr_t' value. It also carries the
// 'is_ptr' param which is used to define the kind field (pointer / num-literal
// assertion).
acrt_result_t acrt_result_new_bool(const char *name, const unsigned int line,
                                   intptr_t value, int is_ptr);

// Creates a new acrt_result_t with an 'EQ_ASSERTION_KIND'. It takes a left /
// right void pointer and an 'eq_function' that returns if the assertion
// succeeds.
acrt_result_t acrt_result_new_eq(const char *name, const unsigned int line,
                                 void *left, void *right,
                                 int (*eq_func)(void *, void *));

#endif
